namespace MauiPdfGenerator.Fluent.Enums;

/// <summary>
/// Define las opciones de alineación horizontal para elementos.
/// </summary>
public enum PdfHorizontalAlignment
{
    /// <summary>
    /// Alinear al inicio (izquierda)
    /// </summary>
    Start,

    /// <summary>
    /// Alinear al centro
    /// </summary>
    Center,

    /// <summary>
    /// Alinear al final (derecha)
    /// </summary>
    End,

    /// <summary>
    /// Estirar para llenar el espacio disponible
    /// </summary>
    Fill
}

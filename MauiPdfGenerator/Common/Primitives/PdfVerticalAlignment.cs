namespace MauiPdfGenerator.Common.Primitives;

/// <summary>
/// Specifies the vertical alignment of content relative to a reference point.
/// </summary>
internal enum PdfVerticalAlignment
{
    /// <summary>
    /// The top edge of the content is aligned with the reference point.
    /// </summary>
    Top,
    /// <summary>
    /// The content is centered vertically on the reference point.
    /// </summary>
    Center,
    /// <summary>
    /// The bottom edge of the content is aligned with the reference point.
    /// </summary>
    Bottom
}

namespace MauiPdfGenerator.Common.Primitives;

/// <summary>
/// Specifies the horizontal alignment of content relative to a reference point.
/// </summary>
internal enum PdfHorizontalAlignment
{
    /// <summary>
    /// Aligned to the left of the reference point.
    /// </summary>
    Left,
    /// <summary>
    /// Centered horizontally on the reference point.
    /// </summary>
    Center,
    /// <summary>
    /// Aligned to the right of the reference point.
    /// </summary>
    Right
}
